#include "StdAfx.h"
#include "Selection.h"


CSelection::CSelection(CWnd *pOwner, shared_ptr<CDrawingObject> pSelected)
	: CTrackerRect(pOwner, pSelected->GetRect()), m_pSelected(pSelected)
{
	if (m_pSelected) {
		m_pSelected->Select(true);
		SetRotation(m_pSelected->GetRotation());
	}
}


CSelection::~CSelection(void)
{
	if (m_pSelected) {
		m_pSelected->Select(false);
	}
}
